class IslamicStory {
  final String id;
  final String title;
  final String emoji;
  final List<String> paragraphs;
  final String moral;
  final int starsReward;

  IslamicStory({
    required this.id,
    required this.title,
    required this.emoji,
    required this.paragraphs,
    required this.moral,
    required this.starsReward,
  });

  factory IslamicStory.fromMap(Map<String, dynamic> map) {
    return IslamicStory(
      id: map['id']?.toString() ?? '',
      title: map['title'] ?? '',
      emoji: map['emoji'] ?? '📖',
      paragraphs: List<String>.from(map['paragraphs'] ?? []),
      moral: map['moral'] ?? '',
      starsReward: map['stars_reward'] ?? 20,
    );
  }
}

